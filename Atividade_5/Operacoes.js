var n1 = prompt("Digite o primeiro número:");
var n2 = prompt("Digite o segundo número:");

alert(
    "Soma = " + (parseFloat(n1) + parseFloat(n2)) +
    "\nSubtração = " + (parseFloat(n1) - parseFloat(n2)) +
    "\nProduto = " + (parseFloat(n1) * parseFloat(n2)) +
    "\nDivisão = " + (parseFloat(n1) / parseFloat(n2)) +
    "\nResto de divisão = " + (parseFloat(n1) % parseFloat(n2))
);
